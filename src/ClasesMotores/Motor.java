package ClasesMotores;

/**
 * Clase abstracta que representa un motor.
 */
public abstract class Motor {
    /**
     * Enciende el motor.
     */
    abstract public void encender();

    /**
     * Acelera el motor.
     */
    abstract public void acelerar();

    /**
     * Apaga el motor.
     */
    abstract public void apagar();
}
